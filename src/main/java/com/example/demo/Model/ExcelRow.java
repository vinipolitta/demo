package com.example.demo.Model;

public class ExcelRow {
    private Number columnA;
    private Number columnB;
    private Number resultColumn;

    public Number getColumnA() {
        return columnA;
    }

    public void setColumnA(Number columnA) {
        this.columnA = columnA;
    }

    public Number getColumnB() {
        return columnB;
    }

    public void setColumnB(Number columnB) {
        this.columnB = columnB;
    }

    public Number getResultColumn() {
        return resultColumn;
    }

    public void setResultColumn(Number resultColumn) {
        this.resultColumn = resultColumn;
    }
}
